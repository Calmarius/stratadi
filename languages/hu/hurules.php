<h2>§1 Egy internet kapcsolat &ndash; egy királyság</h2>
<ul>
	<li>Egy internetkapcsolatról csak egyetlen egy királyságot lehet irányítani. Több királyságba rendszeresen azonos IP címről való belépés multizásnak minősül, amely szabálytalan. Vedd ezt figyelembe, 
	ha új helyre költözöl (pl. kollégiumba.).</li>
	<li> A multikirályságok egyesítve lesznek. A falvak száma, a terjeszkedési pontok, és az arany a résztvevő királyságok faluszáma, terjeszkedési pontja és aranyának átlaga lesz, értelemszerűen. Az egyesített királysághoz a korábbi királyságok királyai
	mindannyian hozzáférnek. A legnagyobb falvak lesznek az egyesített királyságba átvíve. Az egyesült királyságnak nem lesz főkirálya.</li>
	<li>Továbbá tilos ezt a szabályt proxyk használatával megkerülni. (nyilván az IP cím alapján lehet azonosítani a proxy-t)</li>
	<li>Lehetséges egyetlen egy királyságot több embernek is irányítani.</li>
</ul>
<h2>§2 Netikett</h2>
<ul>
	<li>Tilos más játékosok zaklatása, sértegetése, vagy egyéb bárminemű támadó hangnem.</li>
	<li>Minden olyan játékos- és klánprofil, játékosnév, falunév vagy egyéb írásos megnyilvánulás tiltott, amely más játékosokat felzaklat, megsért, fenyeget.</li>
	<li>A valós életre utaló megnyilvánulások, bármilyen szexuális tartalom, politikai megnyilvánulás, különösen a szélsőséges politikai tartalom, vallási fanatizmus, rasszista, vagy egyéb módon kifogásolható, helytelen megnyilvánulás tiltott a játék keretein belül.</li>
	<li>Lánclevelet, tolvajjátékokra hivatkozást, vagy bármilyen más, ehhez kapcsolódó hivatkozást tilos küldeni.</li>
	<li>Adathalász, vagy más olyan levelezés, mely más játékosok jelszavát próbálja meg megszerezni, tiltott.</li>
	<li>Spam, és reklámlevelek küldése tilos, akárcsak a profilban való megjelentetésük.</li>
	<li>Stb, stb...</li>
</ul>
<h2>§3 Adatvédelem</h2>
<ul>
	<li>Tilos más jelszavát kicsalni, különféle hasonló kinézetű weboldalakkal, vagy egyéb módon kicsalni. Ha felmerül ennek a gyanúja az az elkövető accountot azonnal töröljük.</li>
	<li>Az adminoknak nincs szüksége a jelszavadra, ők anélkül is szét tudnak nézni nálad, ha valaki az adminok nevében jelszót kérne, az csak kicsalni akarja, hogy ellophassa a hozzáférésed!</li>
</ul>
